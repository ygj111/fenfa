	public static String getRegionByPrefix(String prefix) {
		String first_regions = "";
		String second_regions = "";
		String regions_org_type = "";

		String first2Num = prefix.substring(0, 2);
		String thirdNum = prefix.charAt(2) + "";
		String forthNum = prefix.charAt(3) + "";

		Iterator<Entry<String, String>> itor = REGIONS_CODE.entrySet().iterator();
		while (itor.hasNext()) {
			Entry<String, String> entry = itor.next();
			String key = entry.getKey();
			String value = entry.getValue();
			if (value.equals(first2Num)) {
				first_regions = key;
			}
		}

		Map map = REGIONS_VRPCODE.get(first_regions);
		Iterator<Entry<String, String>> itor2 = map.entrySet().iterator();
		while (itor2.hasNext()) {
			Entry<String, String> entry = itor2.next();
			String key = entry.getKey();
			String value = entry.getValue();
			if (value.equals(thirdNum)) {
				second_regions = key;
			}
		}

		Iterator<Entry<String, String>> itor3 = REGIONS_ORG_TYPE_CODE.entrySet().iterator();
		while (itor3.hasNext()) {
			Entry<String, String> entry = itor3.next();
			String key = entry.getKey();
			String value = entry.getValue();
			if (value.equals(forthNum)) {
				regions_org_type = key;
			}
		}

		return first_regions + second_regions + regions_org_type;
	}

